  /**
   * Finds the closest matching locale from supported languages
   * @param locale The locale to match
   * @returns The closest matching supported locale
   */
  private findClosestLocale(locale: string): string {
    if (!locale || locale.trim() === '') {
      return this.detectBrowserLanguage();
    }

    const normalizedLocale = locale.trim().toLowerCase();
    
    // Try exact match first
    if (this.supportedLocales.includes(normalizedLocale)) {
      return normalizedLocale;
    }

    // Try exact match with different case
    const exactMatch = this.supportedLocales.find(supported => 
      supported.toLowerCase() === normalizedLocale
    );
    if (exactMatch) {
      return exactMatch;
    }

    // Extract language code (e.g., 'en' from 'en-US' or 'en')
    const languageCode = normalizedLocale.split('-')[0];
    
    // Find locales that start with the same language code
    const languageMatches = this.supportedLocales.filter(supported => 
      supported.toLowerCase().startsWith(languageCode + '-')
    );
    
    if (languageMatches.length > 0) {
      // Return the first match (could be enhanced to find the best match)
      return languageMatches[0];
    }

    // If no language match found, return default
    return 'en-US';
  }

  /**
   * Detects browser language and finds the closest supported locale
   * @returns The closest supported locale
   */
  private detectBrowserLanguage(): string {
    const browserLang = navigator.language || navigator.languages?.[0] || 'en-US';
    return this.findClosestLocale(browserLang);
  }

  setLocale(locale: string): void {
    this.currentLocale = this.findClosestLocale(locale);
  }
  
  getTranslation(key: keyof IzmoTranslations): string {
    const translations = this.translations[this.currentLocale] || this.translations['en-US'];
    return translations[key] || key.toString();
  }

  get locale() {
    return this.currentLocale;
  }
